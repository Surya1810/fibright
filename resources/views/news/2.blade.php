            <p>The objective of the 24/7 Carbon-Free Energy Compact is for every kilowatt-hour of electricity consumption to
                be met with carbon-free energy sources at every hour of every day, everywhere. The movement, now over 140
                members strong, is based on the following actions:</p>

            <ul>
                <li><b>Time-matched procurement:</b>matching hourly electricity consumption with carbon-free energy
                    generation. Hourly matching helps connect clean energy purchasing to the underlying electricity
                    consumption.</li>
                <li><b>Local procurement:</b>purchasing clean energy on the specific local/regional electricity grids where
                    it is consumed. </li>
                <li><b>Technology-inclusive:</b>recognizing the need to create zero-carbon electricity systems as quickly as
                    possible. All Carbon-Free Energy technologies can play a role in creating this future.</li>
                <li><b>Enabling new generation:</b>focusing on delivering additional Carbon-Free Energy to drive the rapid
                    decarbonization of electricity systems.</li>
                <li><b>Maximizing system impact:</b>addressing the dirtiest hours of electricity consumption, when the most
                    fossil fuel is used in generation.</li>
            </ul>

